public class Ellipse {
    float xCoordinate;
    final float yCoordinate;
    final float aAxis;
    final float bAxis;
    final float rank;

    public Ellipse(float xCoordinate, float yCoordinate, float aAxis, float bAxis, float rank) {
        this.xCoordinate = xCoordinate;
        this.yCoordinate = yCoordinate;
        this.aAxis = aAxis;
        this.bAxis = bAxis;
        this.rank = rank;
    }
}
